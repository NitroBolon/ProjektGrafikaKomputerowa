        public Kolor findClosest(int r, int g, int b)
        {
            Kolor kolor = lista[0];
            int roznica = 765;
            int tmp = 0, ind = 0;

            for(int i=0; i<lista.Count; i++)
            {
                tmp = 0;
                tmp += Math.Abs(r - lista[i].R) + Math.Abs(g - lista[i].G) + Math.Abs(b - lista[i].B);
                if (tmp < roznica)
                {
                    roznica = tmp;
                    ind = i;
                }
            }

            for(int i=0; i<kolory.Count; i++)
            {
                if(kolory[i].nazwa == lista[ind].nazwa)
                {
                    kolor = kolory[i];
                }
            }

            return kolor;
        }
